//QNO7:-Write A Program To Convert The Byte To Short
class ByteToShort 
{
	public static void main(String[] args) 
	{
		byte a=97;
		//1.Directly
		short b=a;
		System.out.println(b);

		//2.Using Cast Operator
		short c=(short)a;
		System.out.println(c);
	}
}
/************************************
--------->>>>>OUTPUT>>>>------------*
*************************************
-->>97								*
*************************************/
