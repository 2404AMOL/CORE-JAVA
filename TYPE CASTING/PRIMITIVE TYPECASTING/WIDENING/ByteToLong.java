//QNO6:-Write A Program To Convert The Byte To Long

class ByteToLong
{
	public static void main(String[] args) 
	{
		byte a=97;
		//1.Directly
		long b=a;
		System.out.println(b);

		//2.Using Cast Operator
		long c=(long)a;
		System.out.println(c);
	}
}
