//QNO11:-Write A Program to Print First Five Characters In Reverse Order
class Reverse5Char
{
	public static void main(String[] args) 
	{
		System.out.println("The First Five Charcters Reverse Order:-");

		char ch='e';

		do
		{
			System.out.println(ch);
			ch--;
		}
		while (ch>='a');
	}
}
