//QNO10:-Write A Program to Print All The Odd Numbers Between In 0 to 25

class OddNumBet0to25
{
	public static void main(String[] args) 
	{
		int num=1;
		System.out.println("Even Numbers Between 0 To 25:-");
		do
		{
			System.out.println(num);
			num=num+2;
		}
		while (num<=25);
	}
}

