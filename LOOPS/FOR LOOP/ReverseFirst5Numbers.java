//QNO14:-Write A Program To Print Five Numbers In Reverse Order
class ReverseFirst5Numbers 
{
	public static void main(String[] args) 
	{
		for (int num=5;num>=1;num--)
		{
			System.out.println(num);
		}
	}
}
