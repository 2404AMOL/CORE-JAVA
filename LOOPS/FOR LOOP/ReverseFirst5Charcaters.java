//QNO13:-Write A Program To Print Five Characters In Reverse Order
class ReverseFirst5Charcaters 
{
	public static void main(String[] args) 
	{
		for (char ch='E';ch>='A';ch--)
		{
			System.out.println(ch);
		}
	}
}
