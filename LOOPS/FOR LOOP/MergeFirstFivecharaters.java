//QNO6:-Write A Program To First Five Characters Using For Loop
class MergeFirstFivecharaters 
{
	public static void main(String[] args) 
	{
		String str="";

		char ch;

		for (ch='a';ch<='e';ch++)
		{
			str+=ch;
		}
		System.out.println(str);
	}
}
