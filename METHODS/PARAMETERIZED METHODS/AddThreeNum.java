//QNO6:-Design A Method To Print The Addition Of Three Numbers
class AddThreeNum
{
	public static void main(String[] args) 
	{
		addNum(15,5,20);
	}
	public static void addNum(int a,int b,int c)
	{
		int res = a + b + c;
		System.out.println(res);
	}
}
